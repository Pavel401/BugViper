@lru_cache(maxsize=None)
def get_call_skip(lang: str) -> frozenset[str]:
    """Return the merged skip set for *lang* (common base + language-specific).

    Result is cached so repeated calls per language cost nothing.
    Falls back to just the common set for unknown languages.
    """
    return _COMMON | _BY_LANG.get(lang, frozenset())
